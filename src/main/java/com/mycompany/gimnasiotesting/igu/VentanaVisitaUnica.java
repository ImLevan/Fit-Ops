    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        try{
            String nombre = txtNombre.getText();
            float precio = Float.parseFloat(txtPrecio.getText());
            
            control.hacerPagoVisitaUnica(usuario, nombre, precio);
            
            mostrarMensaje("Visita registrada con éxito", "Info", "Éxito con la visita");
        
            this.dispose();
        }catch (Exception e){
            mostrarMensaje("No se pudo realizar el pago", "Error", "Error al registrar la visita");
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

    private void escucharTxts(){
        txtNombre.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                habilitarBotonSiCamposLlenos();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                habilitarBotonSiCamposLlenos();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                habilitarBotonSiCamposLlenos();
            }
        });

        txtPrecio.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                habilitarBotonSiCamposLlenos();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                habilitarBotonSiCamposLlenos();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                habilitarBotonSiCamposLlenos();
            }
        });
    }
    
    private void habilitarBotonSiCamposLlenos() {
        boolean camposLlenos = !txtPrecio.getText().isEmpty() && !txtNombre.getText().isEmpty();
        btnEnviar.setEnabled(camposLlenos);
    }
    
    public void mostrarMensaje (String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        
        if(tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }else if(tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
    

